/**
  * @brief This function handles TIM3 global interrupt.
  */
void TIM3_IRQHandler(void)
{
  /* USER CODE BEGIN TIM3_IRQn 0 */

  /* USER CODE END TIM3_IRQn 0 */
  HAL_TIM_IRQHandler(&htim3);
  /* USER CODE BEGIN TIM3_IRQn 1 */
	ADCStartOnce();  //ADC转换一次
	LED_t++;
	if(LED_t == 1500)
	{
		HAL_GPIO_TogglePin(LED_GPIO_Port, LED_Pin);
		LED_t = 0;
	}

  /* USER CODE END TIM3_IRQn 1 */
}

/**
  * @brief This function handles USART2 global interrupt.
  */
void USART2_IRQHandler(void)
{
  /* USER CODE BEGIN USART2_IRQn 0 */

  /* USER CODE END USART2_IRQn 0 */
  HAL_UART_IRQHandler(&huart2);
  /* USER CODE BEGIN USART2_IRQn 1 */
	uint32_t temp;
	if(USART2 == huart2.Instance)//判断是否为串口1中断
	{
		if(RESET != __HAL_UART_GET_FLAG(&huart2, UART_FLAG_IDLE))//如果为串口1空闲
		{
			__HAL_UART_CLEAR_IDLEFLAG(&huart2);//清除中断标志
			HAL_UART_DMAStop(&huart2);//停止DMA接收
			temp  = __HAL_DMA_GET_COUNTER(&hdma_usart2_rx);//获取DMA当前还有多少未填充
			Rx_len =  BufferSize - temp; //计算串口接收到的数据个数
//			HAL_UART_Transmit_DMA(&huart2, ReceiveBuff, Rx_len);//发送数据
			Rx_len = 0;//接收数据长度清零
			HAL_UART_Receive_DMA(&huart2, ReceiveBuff, BufferSize);//开启下一次接收
			if((ReceiveBuff[0] == 0xaa)&&(ReceiveBuff[1] == 0xbb)&&(ReceiveBuff[3] == 0xcc)&&(ReceiveBuff[4] == 0xdd))
			{
				switch(ReceiveBuff[2])
					{
					case 0x01:
						task = 1;
					break;
						
					case 0x02:
						task = 2;
					break;
					
					case 0x03:
						task = 3;
					break;
					
					case 0x04:
						task = 4;
					break;
					
					case 0x05:
						task = 5;
					break;
					
					case 0x06:
						task = 6;
					break;
					
					default:
						task = 0;
					break;
					}
				}
			}
		}	

  /* USER CODE END USART2_IRQn 1 */
}

